<h1>Institucional flores na cidade salto, e floricultura na cidade de salto</h1><br>
 <p> A flores e decorações salto nasceu em 15 de abril de 1992, começou suas atividades como um pequeno bazar e comercializava desde pequenos vazos de flores, pequenas utilidades do lar nas feiras da cidade. 

A partir de 1994, começou a especializar-se em decorações para festas, a partir daquele ano investiu maciçamente em equipamentos de contruções para decorações, tornando-se a maior rede de floricultura desta natureza da região.<br><br><br>

A flores e decorações salto  atua hoje com 6 pontos de venda, constituídos de três lojas especializadas em decorações para festas,e 2 lojas especializadas em decorações e aranjos para residencias e 1 loja para festas de casamentos<br><br><br>

 

A flores e decorações salto  hoje está localizada na cidade de salto. Em 2001, com investimentos em equipamentos de última geração, inaugurou a primeira loja na cidade de campinas.<br><br><br>

 

Assim como tudo em nossa história, nosso compromisso é investir constantemente em tecnologia, objetivando garantir segurança, confiança e total satisfação dos nossos clientes. Nós fundadores e colaboradores flores e decorações salto , somos uma família voltada à participação comunitária, engajada em nossa comunidade, colaborando com os mais diversos movimentos da sociedade civil organizada, objetivando sempre um futuro melhor, que privilegie o ser humano como centro de todas as atenções e <br><br><br>principal objeto de todas as ações.</p>




 


	
